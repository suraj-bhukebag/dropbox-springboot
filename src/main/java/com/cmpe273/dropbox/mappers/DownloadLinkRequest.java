package com.cmpe273.dropbox.mappers;

public class DownloadLinkRequest {
	
	private String email;
	
	private String path;

	public String getEmail() {
		return email;
	}

	public void setEmail(String email) {
		this.email = email;
	}

	public String getPath() {
		return path;
	}

	public void setPath(String path) {
		this.path = path;
	}
	
	

}
